package MemberBean;

public class MemberDTO {
	
	private int bk_num;
	private String bk_id;
	private String bk_name;
	private String bk_password;
	private String bk_phone;
	private String bk_email;
	private int bk_level;
	
	public int getBk_num() {
		return bk_num;
	}
	public void setBk_num(int bk_num) {
		this.bk_num = bk_num;
	}
	public String getBk_id() {
		return bk_id;
	}
	public void setBk_id(String bk_id) {
		this.bk_id = bk_id;
	}
	public String getBk_name() {
		return bk_name;
	}
	public void setBk_name(String bk_name) {
		this.bk_name = bk_name;
	}
	public String getBk_password() {
		return bk_password;
	}
	public void setBk_password(String bk_password) {
		this.bk_password = bk_password;
	}
	public String getBk_phone() {
		return bk_phone;
	}
	public void setBk_phone(String bk_phone) {
		this.bk_phone = bk_phone;
	}
	public String getBk_email() {
		return bk_email;
	}
	public void setBk_email(String bk_email) {
		this.bk_email = bk_email;
	}
	public int getBk_level() {
		return bk_level;
	}
	public void setBk_level(int bk_level) {
		this.bk_level = bk_level;
	}
	
	
}
